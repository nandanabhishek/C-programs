#include<stdio.h>
int max(int x, int y, int z);
int main()
{
    int a,b,c;
    printf("enter any three integers to compare them :");
    scanf("%d %d %d",&a,&b,&c);
    int p=max(a,b,c);
    printf("%d is maximum",p);
    return 0;

}
int max(int x, int y, int z)
{
    if(x>y && x>z)
        return x;
    else if(y>x && y>z)
        return y;
    else
        return z;

}
